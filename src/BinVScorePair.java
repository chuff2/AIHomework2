public class BinVScorePair {

	private int bin;
	private int vScore;
	
	public BinVScorePair(int bin, int vScore){
		this.bin = bin;
		this.vScore = vScore;
	}

	public int getBin() {
		return bin;
	}

	public void setBin(int bin) {
		this.bin = bin;
	}

	public int getVScore() {
		return vScore;
	}

	public void setVScore(int vScore) {
		this.vScore = vScore;
	}
	
}
